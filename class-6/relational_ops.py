a = 10
b = 5

less_than = a < b
greater_than = a > b
less_than_or_equal = a <= b
greater_than_or_equal = a >= b
equal = a == b
not_equal = a != b

print("a < b:", less_than) # a < b: False
print("a > b:", greater_than) # a > b: True
print("a <= b:", less_than_or_equal) # a <= b: False
print("a >= b:", greater_than_or_equal) # a >= b: True
print("a == b:", equal) # a == b: False
print("a != b:", not_equal) # a != b: True